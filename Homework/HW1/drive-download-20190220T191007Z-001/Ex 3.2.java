package core;

public class Task32 {
	public static void main (String[] args) {
		//while
		int i=1;
		while (i<=15)
		{
			System.out.print(i+" ");
			i++;
		}
		System.out.println(" ");
		
		//do...while
		int j=1;
		do {
			System.out.print(j+ " ");
		j++;
		}
		while (j<=15);
		
		System.out.println(" ");
		
		//for
		int k;
		for (k=1; k<=15;k++)
		{
			System.out.print(k+ " ");
		}
	}
}
